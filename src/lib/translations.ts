// Helper hook or function to translate string based on language
export function getTranslation(key: string, lang: AppLanguage | string): string {
  const currentLang = lang as AppLanguage;
  if (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) {
    return TRANSLATIONS[currentLang][key];
  }
  // Fallback to English
  if (TRANSLATIONS['English'] && TRANSLATIONS['English'][key]) {
    return TRANSLATIONS['English'][key];
  }
  return key;
}
